        /// <summary>
        /// 获取所有省份列表
        /// </summary>
        public static List<string> GetProvinces()
        {
            return _provincesCities.Keys.OrderBy(p => p).ToList();
        }

        /// <summary>
        /// 根据省份获取城市列表
        /// </summary>
        public static List<string> GetCitiesByProvince(string province)
        {
            if (string.IsNullOrEmpty(province) || !_provincesCities.ContainsKey(province))
            {
                return new List<string>();
            }

            return _provincesCities[province].OrderBy(c => c).ToList();
        }

        /// <summary>
        /// 获取所有城市列表（不分省份）
        /// </summary>
        public static List<string> GetAllCities()
        {
            return _provincesCities.Values
                .SelectMany(cities => cities)
                .Distinct()
                .OrderBy(c => c)
                .ToList();
        }

        /// <summary>
        /// 检查省份是否存在
        /// </summary>
        public static bool IsValidProvince(string province)
        {
            return !string.IsNullOrEmpty(province) && _provincesCities.ContainsKey(province);
        }

        /// <summary>
        /// 检查城市是否属于指定省份
        /// </summary>
        public static bool IsCityInProvince(string city, string province)
        {
            if (string.IsNullOrEmpty(city) || string.IsNullOrEmpty(province))
                return false;

            return _provincesCities.ContainsKey(province) && 
                   _provincesCities[province].Contains(city);
        }
